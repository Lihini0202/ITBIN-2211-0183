     public void showBarChart(){
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(300, "Amount", "january");
        dataset.setValue(500, "Amount", "february");
        dataset.setValue(200, "Amount", "march");
        dataset.setValue(300, "Amount", "april");
        dataset.setValue(600, "Amount", "may");
        dataset.setValue(420, "Amount", "june");
        
        JFreeChart chart = ChartFactory.createBarChart("Salary Payment History","monthly","amount", 
                dataset, PlotOrientation.VERTICAL, false,true,false);
        
        CategoryPlot categoryPlot = chart.getCategoryPlot();
        
        //categoryPlot.setRangeGridlinePaint(Color.BLUE);
        categoryPlot.setBackgroundPaint(Color.WHITE);
        
        ChartPanel barpChartPanel = new ChartPanel(chart);
        barchartpannel.removeAll();
        barchartpannel.add(barpChartPanel, BorderLayout.CENTER);
        barchartpannel.validate();
        
        
    }
    
    
    
    
    

