    public final void showPieChart(){
        
        //create dataset
      DefaultPieDataset barDataset = new DefaultPieDataset( );
      barDataset.setValue("Marketing" , Double.valueOf(20));  
      barDataset.setValue("Administration" , Double.valueOf(20));   
      barDataset.setValue("Sales" , Double.valueOf(40));    
      barDataset.setValue("IT " , Double.valueOf(10));  
      
      //create chart
       JFreeChart piechart = ChartFactory.createPieChart("Departments",barDataset, false,true,false);//explain
      
        PiePlot piePlot =(PiePlot) piechart.getPlot();
      
        piePlot.setBackgroundPaint(Color.white);
        
        //create chartPanel to display chart(graph)
        ChartPanel barChartPanel = new ChartPanel(piechart);
        piechartpannel.removeAll();
        piechartpannel.add(barChartPanel, BorderLayout.CENTER);
        piechartpannel.validate();
    }
    
    
    
