    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        // calculate button 
        //this is use for calculate total of deduction 
        
          try {
            double basicSalary = Double.parseDouble(txt_basicsalary.getText());
            double tax = Double.parseDouble(txt_tax.getText());
            double epf = Double.parseDouble(txt_EPF.getText());
            double etf = Double.parseDouble(txt_ETF.getText());
            double loan = Double.parseDouble(txt_loan.getText());

            double totalDeduction = tax + epf + etf + loan;
            double netSalary = basicSalary - totalDeduction;

            txt_totaldeduction.setText(String.valueOf(totalDeduction));
            txt_netsalary.setText(String.valueOf(netSalary));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

