    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // add salary deduction button this is use for save data to database
        
        
        String id = txt_id.getText();
        String firstname = txt_firstname.getText();
        String surname = txt_surname.getText();
        double basicsalary = Double.parseDouble(txt_basicsalary.getText());
        double tax = Double.parseDouble(txt_tax.getText());
        double epf = Double.parseDouble(txt_EPF.getText());
        double etf = Double.parseDouble(txt_ETF.getText());
        double loan = Double.parseDouble(txt_loan.getText());
        double totaldeduction = Double.parseDouble(txt_totaldeduction.getText());
        double netsalary = Double.parseDouble(txt_netsalary.getText());

            String sql = "INSERT INTO deduction (id,tax,loan,etf,epf,totaldeduction,netsalary,basicsalary,firstname,surname) VALUES (?,?,?, ?, ?, ?, ?, ?, ?, ?)";

            try  {
                pst = conn.prepareStatement(sql);
                
                pst.setString(1, id);
                pst.setDouble(2, tax);
                pst.setDouble(3, loan);
                pst.setDouble(4, etf);
                pst.setDouble(5, epf);
                pst.setDouble(6, totaldeduction);
                pst.setDouble(7, netsalary);
                pst.setDouble(8, basicsalary);
                pst.setString(9, firstname);
                pst.setString(10, surname);
                pst.execute();
            JOptionPane.showMessageDialog(null, "Data Saved");
         
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

